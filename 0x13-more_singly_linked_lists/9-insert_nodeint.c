#include "lists.h"

/**
 * insert_nodeint_at_index - Fit a new bump
 * at a specified spot.
 * @head: Top of  list.
 * @idx: Indicator of the list where the present bump is
 * attached.
 * @n: Integer section.
 *
 * Return: Label of new bump, or NULL if it
 * futile.
 */
listint_t *insert_nodeint_at_index(listint_t **head, unsigned int idx, int n)
{
	unsigned int i;
	listint_t *new;
	listint_t *h;

	h = *head;

	if (idx != 0)
	{
		for (i = 0; i < idx - 1 && h != NULL; i++)
		{
			h = h->next;
		}
	}

	if (h == NULL && idx != 0)
		return (NULL);

	new = malloc(sizeof(listint_t));
	if (new == NULL)
		return (NULL);

	new->n = n;

	if (idx == 0)
	{
		new->next = *head;
		*head = new;
	}
	else
	{
		new->next = h->next;
		h->next = new;
	}

	return (new);
}
